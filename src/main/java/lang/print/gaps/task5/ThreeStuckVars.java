package lang.print.gaps.task5;

public class ThreeStuckVars {
    public static void main(String[] args) {
        /*
        Create 3 int variables with names: first, second, third.
        Initialize them with values = 1, 10, 100. Print all of them to console
        within one System.out.println statement (with no spaces between).
         */
        int first, second, third;
        first = 1;
        second = 10;
        third = 100;
        System.out.println(first + "" + second + "" + third);
    }
}
